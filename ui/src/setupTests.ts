import '@testing-library/jest-dom/vitest'

// jsdom doesn't implement ResizeObserver, which @xyflow/react's <ReactFlow>
// requires to measure its container -- stub it so node/edge rendering tests
// don't need a real browser.
class ResizeObserverStub {
  observe() {}
  unobserve() {}
  disconnect() {}
}

if (typeof globalThis.ResizeObserver === 'undefined') {
  globalThis.ResizeObserver = ResizeObserverStub as unknown as typeof ResizeObserver
}
